void ServoAdd()
{
    if(ServoaMode   == EE_INDEXSELECT)
    {
        NowServoIndex--;
        if(NowServoIndex <0)
        {
            NowServoIndex=7;
        }
        wait(0.1);
    }
    else if(ServoaMode   == EE_VALUESELECT)
    {
        ServoValue[NowServoIndex]++;
        if(ServoValue[NowServoIndex] >=180)
        ServoValue[NowServoIndex]=180;
        if(ServoValue[NowServoIndex] <0)
        ServoValue[NowServoIndex]=0;
    }
}

void ServoMun()
{
    if(ServoaMode   == EE_INDEXSELECT)
    {
        NowServoIndex++;
        if(NowServoIndex >7)
        {
            NowServoIndex=0;
        }
        wait(0.1);
    }
    else if(ServoaMode   == EE_VALUESELECT)
    {
        ServoValue[NowServoIndex]--;
        if(ServoValue[NowServoIndex] >=180)
        ServoValue[NowServoIndex]=180;
        if(ServoValue[NowServoIndex] <0)
        ServoValue[NowServoIndex]=0;
    }
}

void ServoOK()
{
    if(ServoaMode   == EE_INDEXSELECT)
    {
        ServoaMode=EE_VALUESELECT;
    }
    else if(ServoaMode   == EE_VALUESELECT)
    {
        SetServo_1(NowServoIndex,ServoValue[NowServoIndex],10);
        ServoaMode   = EE_INDEXSELECT;
    }
    EEPROM_Write(NowServoIndex+40,ServoValue[NowServoIndex]);
    EEPROM_WriteBuffer();
    wait(0.2);
}

